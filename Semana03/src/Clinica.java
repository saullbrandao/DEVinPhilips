public class Clinica {
    String nome;
    String dono;
    String CNPJ;
    String logradouro;
    Integer codigo;

    public Clinica(String nome, String dono, String CNPJ, String logradouro, Integer codigo) {
        this.nome = nome;
        this.dono = dono;
        this.CNPJ = CNPJ;
        this.logradouro = logradouro;
        this.codigo = codigo;
    }
}
